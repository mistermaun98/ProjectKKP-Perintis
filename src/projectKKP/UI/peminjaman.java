    private void tblPinjamPersonalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPinjamPersonalMouseClicked
        try {
            int row = tblPinjamPersonal.getSelectedRow();
            String table_click = (tblPinjamPersonal.getModel().getValueAt(row, 0).toString());
            String sql = "select * from peminjaman where kdpinjam='"+table_click+"'";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                String add1 = rs.getString("kdpinjam");
                kdpinjam.setText(add1);
                
                String add2 = rs.getString("kd_anggota");
                kdAnggota.setText(add2);
                
                String add3 = rs.getString("nama");
                nama.setText(add3);
                
                String add4 = rs.getString("kls");
                kelas.setSelectedItem(add4);
                
                String add5 = rs.getString("kdbuku");
                kdbuku.setText(add5);
                
                String add6 = rs.getString("jdbuku");
                jdbuku.setText(add6);
                
                String add7 = rs.getString("jml");
                jmlbuku.setText(add7);
                
                java.sql.Date add8 = new java.sql.Date(rs.getDate("tgl_pinjam").getTime());
                tglpinjam.setDate(add8);
                
                java.sql.Date add9 = new java.sql.Date(rs.getDate("tgl_kembali").getTime());
                tglkembali.setDate(add9);
                
                String add10 = rs.getString("stat");
                status.setSelectedItem(add10);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblPinjamPersonalMouseClicked

