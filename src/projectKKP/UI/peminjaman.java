    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
                PreparedStatement ps = conn.prepareStatement("insert into peminjaman values (?,?,?,?,?,?,?,?,?,?)");
                
                ps.setString(1, kdpinjam.getText());
                ps.setString(2, kdAnggota.getText());
                ps.setString(3, nama.getText());
                ps.setString(4, kelas.getSelectedItem().toString());
                ps.setString(5, kdbuku.getText());
                ps.setString(6, jdbuku.getText());
                ps.setString(7, jmlbuku.getText());
                
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String date1 = sdf.format(tglpinjam.getDate());
                String date2 = sdf.format(tglkembali.getDate());
                ps.setString(8, date1);
                ps.setString(9, date2);
                ps.setString(10, status.getSelectedItem().toString());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil diinput !");
                resetForm();
                tampiltabelPersonal();
                tampiltabelKelas();
            } catch (Exception e) {
                Logger.getLogger(peminjaman.class.getName()).log(Level.SEVERE, null, e);
                JOptionPane.showMessageDialog(null, e);
            }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda ingin mengubah data ini ?", "UBAH DATA", JOptionPane.YES_NO_OPTION);
        if(ok == 0) {
                try {
                    String sql = "update peminjaman set "
                            + "kd_anggota=?,nama=?,kls=?,kdbuku=?,jdbuku=?,jml=?,"
                            + "tgl_pinjam=?,tgl_kembali=?,stat=? "
                            + "where kdpinjam=?";
                
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setString(1, kdAnggota.getText());
                    ps.setString(2, nama.getText());
                    ps.setString(3, kelas.getSelectedItem().toString());
                    ps.setString(4, kdbuku.getText());
                    ps.setString(5, jdbuku.getText());
                    ps.setString(6, jmlbuku.getText());
                    
                    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                    String date1 = sdf.format(tglpinjam.getDate());
                    String date2 = sdf.format(tglkembali.getDate());
                    ps.setString(7, date1);
                    ps.setString(8, date2);
                    ps.setString(9, status.getSelectedItem().toString());
                    ps.setString(10, kdpinjam.getText());
                    
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diubah !");
                    resetForm();
                    tampiltabelPersonal();
                    tampiltabelKelas();
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, e);
                }
        }
    }//GEN-LAST:event_editBtnActionPerformed

