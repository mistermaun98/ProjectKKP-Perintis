    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (cekInput()) {
            try {
                PreparedStatement ps = conn.prepareStatement("insert into buku "
                        + "values (?,?,?,?,?,?,?,?)");
                    
                    ps.setString(1, kdbuku.getText());
                    ps.setString(2, jdbuku.getText());
                    ps.setString(3, pengarang.getText());
                    ps.setString(4, penerbit.getText());
                    ps.setString(5, thTerbit.getText());
                    ps.setString(6, kdISBN.getText());
                    ps.setString(7, kategoriBuku.getSelectedItem().toString());
                    ps.setString(8, jmlBuku.getText());
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data berhasil diinput !");
                    resetForm();
                    tampiltabel();
                    
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Masih ada data yang kosong !");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

