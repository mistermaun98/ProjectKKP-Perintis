    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda ingin mengubah data ini ?", "UBAH DATA", JOptionPane.YES_NO_OPTION);
        if(ok == 0) {
                try {
                    String sql = "update buku set jdbuku=?,pengarang=?,"
                            + "penerbit=?,th_terbit=?,isbn=?,kategori=?,jml=? "
                            + "where kdbuku=?";
                
                    PreparedStatement ps = conn.prepareStatement(sql);
                    ps.setString(1, jdbuku.getText());
                    ps.setString(2, pengarang.getText());
                    ps.setString(3, penerbit.getText());
                    ps.setString(4, thTerbit.getText());
                    ps.setString(5, kdISBN.getText());
                    ps.setString(6, kategoriBuku.getSelectedItem().toString());
                    ps.setString(7, jmlBuku.getText());
                    ps.setString(8, kdbuku.getText());
                    
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Diubah !");
                    resetForm();
                    tampiltabel();
                    
                } catch (SQLException e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, e);
                }
            }
    }//GEN-LAST:event_editBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data ini ?", "HAPUS DATA", JOptionPane.YES_NO_OPTION);
        if(ok == 0) {
            try {
                PreparedStatement ps = conn.prepareStatement("delete from buku where kdbuku=?");
                ps.setString(1, kdbuku.getText());
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus !");
                resetForm();
                tampiltabel();
            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void tblBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBukuMouseClicked
        try {
            int row = tblBuku.getSelectedRow();
            String table_click = (tblBuku.getModel().getValueAt(row, 0).toString());
            String sql = "select * from buku where kdbuku='"+table_click+"'";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                String add1 = rs.getString("kdbuku");
                kdbuku.setText(add1);
                
                String add2 = rs.getString("jdbuku");
                jdbuku.setText(add2);
                
                String add3 = rs.getString("pengarang");
                pengarang.setText(add3);
                
                String add4 = rs.getString("penerbit");
                penerbit.setText(add4);
                
                String add5 = rs.getString("th_terbit");
                thTerbit.setText(add5);
                
                String add6 = rs.getString("isbn");
                kdISBN.setText(add6);
                
                String add7 = rs.getString("kategori");
                kategoriBuku.setSelectedItem(add7);
                
                String add8 = rs.getString("jml");
                jmlBuku.setText(add8);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblBukuMouseClicked

    private void searchbarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchbarKeyReleased
        if(searchbar.getText().length()>0) {
            try {
                String sql = "Select kdbuku,jdbuku,pengarang,penerbit,kategori"
                        + " from buku where kdbuku like '%"+searchbar.getText()+"%' "
                        + "or jdbuku like '%"+searchbar.getText()+"%' "
                        + "or pengarang like '%"+searchbar.getText()+"%' "
                        + "or penerbit like '%"+searchbar.getText()+"%' "
                        + "or kategori like '%"+searchbar.getText()+"%' ";
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                tblBuku.setModel(DbUtils.resultSetToTableModel(rs));
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            tampiltabel();
        }
    }//GEN-LAST:event_searchbarKeyReleased

