    protected void tampiltabel() {
        try {
            PreparedStatement ps = conn.prepareStatement("select kdbuku,jdbuku,pengarang,"
                    + "penerbit,kategori,jml from buku");
            ResultSet rs = ps.executeQuery();
            tblBuku.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    protected void resetForm() {
        kdbuku.setText("");
        jdbuku.setText("");
        pengarang.setText("");
        penerbit.setText("");
        thTerbit.setText("");
        kdISBN.setText("");
        kategoriBuku.setSelectedIndex(0);
        jmlBuku.setText("");
        searchbar.setText("");
        kdbuku.requestFocus();
    }
    
    public boolean cekInput() {
        if(kdbuku.getText() == null
                ||jdbuku.getText() == null
                ||pengarang.getText() == null
                ||penerbit.getText() == null
                ||thTerbit.getText() == null
                ||kdISBN.getText() == null
                ||kategoriBuku.getSelectedIndex() == 0
                ||jmlBuku.getText() == null) {
            return false;
        } else {
            return true;
        }
    }
