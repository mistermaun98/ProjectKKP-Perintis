    private void searchbarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchbarKeyReleased
        if(searchbar.getText().length()>0) {
            try {
                String sql = "Select kdbuku,jdbuku,pengarang "
                        + "from buku where kdbuku like '%"+searchbar.getText()+"%' "
                        + "or jdbuku like '%"+searchbar.getText()+"%' "
                        + "or pengarang like '%"+searchbar.getText()+"%' ";
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();
                tblBuku.setModel(DbUtils.resultSetToTableModel(rs));
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            tampiltabel();
        }
    }//GEN-LAST:event_searchbarKeyReleased

