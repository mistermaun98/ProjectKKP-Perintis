    private void tblBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBukuMouseClicked
        try {
            int row = tblBuku.getSelectedRow();
            String table_click = (tblBuku.getModel().getValueAt(row, 0).toString());
            String sql = "select * from buku where kdbuku='"+table_click+"'";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                String add1 = rs.getString("kdbuku");
                kdbuku.setText(add1);
                
                String add2 = rs.getString("jdbuku");
                jdbuku.setText(add2);
                
                String add3 = rs.getString("pengarang");
                pengarang.setText(add3);
                
                String add4 = rs.getString("penerbit");
                penerbit.setText(add4);
                
                String add5 = rs.getString("th_terbit");
                thTerbit.setText(add5);
                
                String add6 = rs.getString("kategori");
                kategori.setText(add6);
                
                String add7 = rs.getString("jml");
                jmlbuku.setText(add7);
                
                int jmlbuku = rs.getInt("jml");
                if (jmlbuku > 0) {
                    status.setForeground(Color.GREEN);
                    status.setText("Buku Tersedia");
                } else if (jmlbuku == 0) {
                    status.setForeground(Color.RED);
                    status.setText("Buku Tidak Tersedia");
                }
                
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblBukuMouseClicked

